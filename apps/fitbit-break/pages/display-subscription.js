import Head from "next/head";
import styles from "../styles/Home.module.css";
import { useSession } from "next-auth/react";
import { useRouter } from "next/router";
import React from "react";
import Button from '@mui/material/Button';
import { getServerSession } from "next-auth/next";
import { authOptions } from "./api/auth/[...nextauth]";
import FitbitSubscriptionHelper from "@time-fit/data-source/fitbit/helper/FitbitSubscriptionHelper";
import UserInfoHelper from "@time-fit/helper/UserInfoHelper";

export async function getServerSideProps(ctx) {
  const session = await getServerSession(ctx.req, ctx.res, authOptions);

  if (!session) {
    return {
      props: {},
    };
  }

  const user = await UserInfoHelper.getUserInfoByUsername(session.user.name);

  const subscriptionList = await FitbitSubscriptionHelper.listSubscriptionsForUser(user, ["activities", "userRevokedAccess"]);

  return {
    props: { subscriptionList: subscriptionList},
  };
}

export default function DisplaySubscription({subscriptionList}) {
  const { data: session, status } = useSession();
  const router = useRouter();

  if (status == "loading") return <div>loading...</div>;
  if (!session){
    router.push('/');
    return null;
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Fitbit Break</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
      <h1 className={styles.title}>Display subscription for {session.user.name}</h1>
        <div>
            {
                JSON.stringify(subscriptionList, null, 2)
            }
        </div>
        <br />
        <br />

        <Button variant="contained" onClick={(event) => {
            router.push("/main");
            return;
          }} >Return to home</Button>

      </main>

      <footer className={styles.main}>
        <div>Fitbit Break Study</div>
      </footer>
    </div>
  );
}
